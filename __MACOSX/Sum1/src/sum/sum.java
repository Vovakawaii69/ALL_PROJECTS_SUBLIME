public class sum {
    public double Summa(double a, double b) {
        return a+b;
    }
    public double Vahe(double a, double b) {
        return (a-b); 
  }
  public double Jagamine(double a, double b) {
        return (a/b);  
  }
  public double Korrutamine(double a, double b) {
        return (a*b);  
   }
}
